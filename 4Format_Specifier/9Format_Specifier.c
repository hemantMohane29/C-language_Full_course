// format specifier 

// int = %d 
// float = %f
// char = %c
// double = %lf
// long = %l

#include<stdio.h>
int main(){

int a = 10;
char b = 'h';
float c = 4.251;
double d = 2.01454;
long e = 2.00000145;

printf("int = %d",a);
printf("\nchar = %c",b);
printf("\nfloat = %f",c);
printf("\ndouble = %lf",d);
printf("\nlong = %i",e);

 return 0;
}


